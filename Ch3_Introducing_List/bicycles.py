# Working with list
bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles)

# Mostrando el primer elemento de la lista
print(bicycles[0].title())

# Mostrando otros elementos
print(bicycles[1])
print(bicycles[3])

# Mostrar el ultimo elemento de la lista
print(bicycles[-1])

# Creando un mensaje incluyendo un elemento de la lista
message = "My first bicycle was a " + bicycles[0].title() + "."
print(message)